package game.jgengine.graphics;

public class Vertex
{
	public float x;
	public float y;
	public float z;
	public float r;
	public float g;
	public float b;
	public float a;

	public Vertex(float x, float y, float z, float r, float g, float b, float a)
	{
		this.x = x;
		this.y = y;
		this.z = z;
		this.r = r;
		this.g = g;
		this.b = b;
		this.a = a;
	}
}
